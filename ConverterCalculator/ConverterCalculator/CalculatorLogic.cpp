#include "CalculatorLogic.h"

using namespace ConverterCalculator;

// == MATHEMATIC LOGIC METHODS ==

float CalculatorLogic::MultiplyNumbers(int num1, int num2)
{
	return num1 * num2;
}

float CalculatorLogic::DividNumbers(int num1, int num2)
{
	return num1 / num2;
}

float CalculatorLogic::AddNumbers(int num1, int num2)
{
	return num1 + num2;
}

float CalculatorLogic::SubtractNumbers(int num1, int num2)
{
	return num1 - num2;
}